package com.loiane.cursojava.aula11;

public class SequenciaEscape {

    public static void main(String[] args) {

        // Aspas simples
        System.out.println("Aspas simples: \'");
        // Aspas duplas
        System.out.println("Aspas duplas: \"");
        // Barra invertida
        System.out.println("Barra invertida: \\");
        // Tabulação
        System.out.println("Tabulação: \t");
        // Nova linha
        System.out.println("Nova linha: \r");

    }

}
